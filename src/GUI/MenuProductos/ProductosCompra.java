    private void bApartarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bApartarActionPerformed
        Producto producto = gestorProductos.buscarIdProducto(jtIdApartar.getText());
        if(producto != null){
            if(!jtCantidadApartar.getText().equals("")){
                if(producto.getCantidad() == 0){
                    JOptionPane.showMessageDialog(this, "No hay ninguna unidad de este producto");
                }else if(producto.getCantidad() < Integer.parseInt(jtCantidadApartar.getText())){
                    JOptionPane.showMessageDialog(this, "No hay suficientes unidades disponibles.\n"
                                                                                 + "Cantidad disponible: " + String.valueOf(producto.getCantidad()));   
                }else if(Integer.parseInt(jtCantidadApartar.getText()) < 0){
                    JOptionPane.showMessageDialog(this, "La cantidad ingresada es negativa, no se realizo apartado.");           
                }else{ 
                    totalDinero = totalDinero+ producto.getPrecio()*Integer.parseInt(jtCantidadApartar.getText()) ;
                    gestorProductos.apartarProducto(producto, Integer.parseInt(jtCantidadApartar.getText()));
                    JOptionPane.showMessageDialog(this, "Producto apartado con exito."
                                                                               + "\n  Precio: $" + producto.getPrecio() 
                                                                               + "\n  cantidad: " + jtCantidadApartar.getText() + " unidades"
                                                                               + "\n  Precio total: $" + producto.getPrecio()*Integer.parseInt(jtCantidadApartar.getText()));
                    verTotalAcumulado();
                    verColaApartados();
                    verListaProductos();
                }      
            }else{
                JOptionPane.showMessageDialog(this, "Rellene todos los campos.");
            }
         }else{
            JOptionPane.showMessageDialog(this, "Producto no encontrado, no se realizo el apartado.");
        }
    }//GEN-LAST:event_bApartarActionPerformed

