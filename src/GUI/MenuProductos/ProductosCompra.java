    private void bEditarAparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarAparActionPerformed
        Producto producto = gestorProductos.verProductoApartado();
        if(producto != null){
        if(!jtCantidadApartar.getText().equals("")){
            if(Integer.parseInt(jtCantidadApartar.getText()) == producto.getCantidad()){
                JOptionPane.showMessageDialog(this, "Ha ingresado la misma cantidad, no se realizo edición!");
            }else if(producto.getCantidad() >= Integer.parseInt(jtCantidadApartar.getText()) && Integer.parseInt(jtCantidadApartar.getText()) >= 0){
                totalDinero = totalDinero-producto.getPrecio()*(producto.getCantidad()-Integer.parseInt(jtCantidadApartar.getText()));
                GestorProductos.editarCantidadUltimoApartado(Integer.parseInt(jtCantidadApartar.getText()));                
                JOptionPane.showMessageDialog(this, "Cantidad del producto apartado editada con exito!");
                verTotalAcumulado();
                verColaApartados();
                verListaProductos();
            }else if(Integer.parseInt(jtCantidadApartar.getText()) > producto.getCantidad()){
                JOptionPane.showMessageDialog(this, "La cantidad ingresada excede la cantidad del producto apartado, no se realizo edicion.");               
            }else if(Integer.parseInt(jtCantidadApartar.getText()) < 0){
                System.out.println("La cantidad ingresada es negativa, no se realizo edicion.");
            }                      
         }else{
                JOptionPane.showMessageDialog(this, "Rellene todos los campos.");
         }
        }else{
            JOptionPane.showMessageDialog(this, "No hay ningún producto apartado.");
        }
    }//GEN-LAST:event_bEditarAparActionPerformed


