    public void verListaProductos(){
        StringBuilder sb = new StringBuilder();
            
            for(int i = 0; i<gestorProductos.listaProducto.length(); i++){
                if(gestorProductos.listaProducto != null){
                    sb.append("ID: ").append(gestorProductos.listaProducto.find(i).getId()).append("\n");               
                    sb.append("Nombre: ").append(gestorProductos.listaProducto.find(i).getNombre()).append("\n");               
                    sb.append("Precio: $").append(String.valueOf(gestorProductos.listaProducto.find(i).getPrecio())).append("\n"); 
                    sb.append("Cantidad: ").append(String.valueOf(gestorProductos.listaProducto.find(i).getCantidad())).append("\n"); 
                    sb.append("\n");
                }                      
            }
            String resultado = sb.toString().trim();
            jtListaProductos.setText(resultado);
    }
    
